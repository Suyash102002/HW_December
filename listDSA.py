#Largest and smallest element in list: 
my_list = [5, 1, 8, 9]
largest_element = max(my_list)
smallest_element = min(my_list)
print(f"largest : {largest_element}, smallest: {smallest_element}")
print("")

# Sorting a list in ascending and descending order
my_list = [5, 1, 8, 3, 2]
my_list.sort()  
print(f"Ascending: {my_list}")
my_list.sort(reverse=True)  
print(f"Descending: {my_list}")
